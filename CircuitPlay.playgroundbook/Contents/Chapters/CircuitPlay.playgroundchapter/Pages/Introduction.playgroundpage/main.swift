WWDC21 Swift Student Challenge Submission by Jose Adolfo Talactac

## Overview
Hello, welcome to CircuitPlay! CircuitPlay is an interactive sandbox experience that allows you to design, setup, and simulate circuit. Each page in this playground can be experienced for 30 seconds or less. However, since this is a sandbox environment, feel free to do what ever you wish!
 
